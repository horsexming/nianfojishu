package com.lc.entity.zw;

import java.io.Serializable;

/**
 * 
 * @author jhh
 *外币字典
 */
public class LSwbzd implements Serializable{
	
	//外币编号	LSWBZD_WBBH	varchar(10)	No			
	//币名	LSWBZD_BM	varchar(30)	No			
	//代表符	LSWBZD_DBF	char(4)				
	//标价方法	LSWBZD_BJFF	CHAR(1)	No	‘1’	‘1’,’2’	‘1’:直接标价法‘2’:间接标价法
	//汇率方式	LSWBZD_HLFS	char(1)	No	‘1’	‘1’,’2’	‘1’:固定汇率‘2’:现行汇率
	//精度	LSWBZD_JD	Int	No	2		
	//折算误差	LSWBZD_ZSWC	U001	No	0		
	//是否本位币	LSWBZD_BWB	Char(1)	No	‘0’	‘0’,’1’	‘1’:是;‘0’:否
	private String WBBH;//外币编号
	private String BM;//币名
	private String DBF;//代表符号
	private String BJFF;//标价方式
	private String HLFS;//汇率方式
	private Integer JD;//精度
	private String ZSWC;//折算误差
	private String BWB;//是否本位币
	public String getWBBH() {
		return WBBH;
	}
	public void setWBBH(String wBBH) {
		WBBH = wBBH;
	}
	public String getBM() {
		return BM;
	}
	public void setBM(String bM) {
		BM = bM;
	}
	public String getDBF() {
		return DBF;
	}
	public void setDBF(String dBF) {
		DBF = dBF;
	}
	public String getBJFF() {
		return BJFF;
	}
	public void setBJFF(String bJFF) {
		BJFF = bJFF;
	}
	public String getHLFS() {
		return HLFS;
	}
	public void setHLFS(String hLFS) {
		HLFS = hLFS;
	}
	public Integer getJD() {
		return JD;
	}
	public void setJD(Integer jD) {
		JD = jD;
	}
	public String getZSWC() {
		return ZSWC;
	}
	public void setZSWC(String zSWC) {
		ZSWC = zSWC;
	}
	public String getBWB() {
		return BWB;
	}
	public void setBWB(String bWB) {
		BWB = bWB;
	}
	
}
