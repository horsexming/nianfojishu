package com.task.entity;

import java.io.Serializable;

/***
 * 项目登录信息表
 * 
 * @author 刘培
 * 
 */

public class ProjectLogin  implements Serializable {
	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	private Integer id;
	private Integer userId;// 用户ID
	private String code;// 工号
	private String cardId;// 卡号
	private String loginFieldValue;// 登录字段值
	private ProjectRecord projectRecord;

	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public Integer getUserId() {
		return userId;
	}

	public void setUserId(Integer userId) {
		this.userId = userId;
	}

	public String getCode() {
		return code;
	}

	public void setCode(String code) {
		this.code = code;
	}

	public String getCardId() {
		return cardId;
	}

	public void setCardId(String cardId) {
		this.cardId = cardId;
	}

	public String getLoginFieldValue() {
		return loginFieldValue;
	}

	public void setLoginFieldValue(String loginFieldValue) {
		this.loginFieldValue = loginFieldValue;
	}

	public ProjectRecord getProjectRecord() {
		return projectRecord;
	}

	public void setProjectRecord(ProjectRecord projectRecord) {
		this.projectRecord = projectRecord;
	}

}
