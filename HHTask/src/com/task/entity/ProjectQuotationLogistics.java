package com.task.entity;

import java.io.Serializable;

/**
 * 报价单_运输
 * @author 马凯
 */
public class ProjectQuotationLogistics  implements Serializable {
	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;
	private Integer id;//序号
	private Double transportationPrice;//运输费用
	private Double packagePrice;//包装费用
	private ProjectQuotation root;

	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public ProjectQuotation getRoot() {
		return root;
	}
	public void setRoot(ProjectQuotation root) {
		this.root = root;
	}
	public Double getTransportationPrice() {
		return transportationPrice;
	}
	public void setTransportationPrice(Double transportationPrice) {
		this.transportationPrice = transportationPrice;
	}
	public Double getPackagePrice() {
		return packagePrice;
	}
	public void setPackagePrice(Double packagePrice) {
		this.packagePrice = packagePrice;
	}
}
