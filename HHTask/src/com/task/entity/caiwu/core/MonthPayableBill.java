package com.task.entity.caiwu.core;

import java.io.Serializable;

import com.task.util.FieldMeta;

/**
 * 月度应收应付账单(ta_fin_caiwu_MonthPayableBill)
 * 
 * @author 刘培
 * 
 */
public class MonthPayableBill implements Serializable{
	private Integer id;
	private String jzMonth;// 记账月份
	private Float yingfukuanJine;// 应付款金额
	private Float realfukuanJine;// 已付款金额
	private Float weifukuanJine;// 未付款金额

	private Float yingshoukuanJine;// 应收款金额
	private Float realshoukuanJine;// 已收款金额
	private Float weishoukuanJine;// 未收款金额

	private String status;

	@FieldMeta(name = "添加时间")
	private String saveTime;// 添加时间(入库时间)
	private String saveUser;// 添加人(入库操作人员)

	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public String getJzMonth() {
		return jzMonth;
	}

	public void setJzMonth(String jzMonth) {
		this.jzMonth = jzMonth;
	}

	public Float getYingfukuanJine() {
		return yingfukuanJine;
	}

	public void setYingfukuanJine(Float yingfukuanJine) {
		this.yingfukuanJine = yingfukuanJine;
	}

	public Float getRealfukuanJine() {
		return realfukuanJine;
	}

	public void setRealfukuanJine(Float realfukuanJine) {
		this.realfukuanJine = realfukuanJine;
	}

	public Float getYingshoukuanJine() {
		return yingshoukuanJine;
	}

	public void setYingshoukuanJine(Float yingshoukuanJine) {
		this.yingshoukuanJine = yingshoukuanJine;
	}

	public Float getRealshoukuanJine() {
		return realshoukuanJine;
	}

	public void setRealshoukuanJine(Float realshoukuanJine) {
		this.realshoukuanJine = realshoukuanJine;
	}

	public String getStatus() {
		return status;
	}

	public void setStatus(String status) {
		this.status = status;
	}

	public String getSaveTime() {
		return saveTime;
	}

	public void setSaveTime(String saveTime) {
		this.saveTime = saveTime;
	}

	public String getSaveUser() {
		return saveUser;
	}

	public void setSaveUser(String saveUser) {
		this.saveUser = saveUser;
	}

	public Float getWeifukuanJine() {
		return weifukuanJine;
	}

	public void setWeifukuanJine(Float weifukuanJine) {
		this.weifukuanJine = weifukuanJine;
	}

	public Float getWeishoukuanJine() {
		return weishoukuanJine;
	}

	public void setWeishoukuanJine(Float weishoukuanJine) {
		this.weishoukuanJine = weishoukuanJine;
	}

}
