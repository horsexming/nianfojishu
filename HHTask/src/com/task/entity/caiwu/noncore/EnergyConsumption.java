package com.task.entity.caiwu.noncore;

import java.io.Serializable;

/**
 * 能耗价格表
 * @author 李聪
 * ta_EnergyConsumption
 */
public class EnergyConsumption implements Serializable{
	private Integer id;
	private String type;//水费、电费。……
	private Float unitPrice;//单价
	private String name;//名称
	private String isTimeLimit;//是否限时（是/否）
	private String startTime;//限时起始时间（时分秒）
	private String endTime;//限时结束时间（时分秒）
	private String saveTime;//添加时间
	private String saveUser;//添加人
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public Float getUnitPrice() {
		return unitPrice;
	}
	public void setUnitPrice(Float unitPrice) {
		this.unitPrice = unitPrice;
	}
	public String getIsTimeLimit() {
		return isTimeLimit;
	}
	public void setIsTimeLimit(String isTimeLimit) {
		this.isTimeLimit = isTimeLimit;
	}
	public String getStartTime() {
		return startTime;
	}
	public void setStartTime(String startTime) {
		this.startTime = startTime;
	}
	public String getEndTime() {
		return endTime;
	}
	public void setEndTime(String endTime) {
		this.endTime = endTime;
	}
	public String getSaveTime() {
		return saveTime;
	}
	public void setSaveTime(String saveTime) {
		this.saveTime = saveTime;
	}
	public String getSaveUser() {
		return saveUser;
	}
	public void setSaveUser(String saveUser) {
		this.saveUser = saveUser;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
}
