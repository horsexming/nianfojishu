package com.task.entity;

import java.io.Serializable;

/**
 * 流程名称表:(ta_ProcessName)
 * 
 * @author Administrator
 *
 */
public class ProcessName implements Serializable {
	private static final long serialVersionUID = 1L;

	private Integer id;
	private String name;
	private String iseconomize;// 是否能节约;

	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getIseconomize() {
		return iseconomize;
	}

	public void setIseconomize(String iseconomize) {
		this.iseconomize = iseconomize;
	}

}
