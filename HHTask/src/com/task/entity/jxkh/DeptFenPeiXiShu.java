package com.task.entity.jxkh;

import java.io.Serializable;

/**
 * 各部门分配系数表:(ta_DeptFenPeiXiShu)
 * @author wxf
 *
 */
public class DeptFenPeiXiShu implements Serializable{
	private static final long serialVersionUID = 1L;
	private Integer id;
	private String dept;//部门
	private Double fenpeiXiShu;//分配系数
	
	private String addTime;//添加时间
	private String addUsersName;//添加人
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public String getDept() {
		return dept;
	}
	public void setDept(String dept) {
		this.dept = dept;
	}
	
	public Double getFenpeiXiShu() {
		return fenpeiXiShu;
	}
	public void setFenpeiXiShu(Double fenpeiXiShu) {
		this.fenpeiXiShu = fenpeiXiShu;
	}
	public String getAddTime() {
		return addTime;
	}
	public void setAddTime(String addTime) {
		this.addTime = addTime;
	}
	public String getAddUsersName() {
		return addUsersName;
	}
	public void setAddUsersName(String addUsersName) {
		this.addUsersName = addUsersName;
	}
	
	
}
