package com.task.entity.jxkh;

import java.io.Serializable;

/**
 * 目标达成未达成加减分表:(ta_TargetAchievedMark)
 * @author wxf
 *
 */
public class TargetAchievedMark implements Serializable{
	private static final long serialVersionUID = 1L;
	private Integer id;
	private String targetName;//目标名称
	private String deptName;//部门名称
	private Integer marks;//加减分数;
	
	private String addTime;//添加时间
	private String addUsersName;//添加人
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public String getTargetName() {
		return targetName;
	}
	public void setTargetName(String targetName) {
		this.targetName = targetName;
	}
	public String getDeptName() {
		return deptName;
	}
	public void setDeptName(String deptName) {
		this.deptName = deptName;
	}
	public Integer getMarks() {
		return marks;
	}
	public void setMarks(Integer marks) {
		this.marks = marks;
	}
	public String getAddTime() {
		return addTime;
	}
	public void setAddTime(String addTime) {
		this.addTime = addTime;
	}
	public String getAddUsersName() {
		return addUsersName;
	}
	public void setAddUsersName(String addUsersName) {
		this.addUsersName = addUsersName;
	}
	
	
}
