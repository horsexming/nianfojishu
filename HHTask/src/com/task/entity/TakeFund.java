package com.task.entity;

import java.util.Date;

public class TakeFund implements java.io.Serializable {

	// Fields

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;
	private int id;
	
	private int personId;
	
	private Date takeDate;
	
	private float takeFund;
	
	private float cardBalance;
	
	private String userNo;
	
	public TakeFund() {
		super();
	}

	public TakeFund(int personId, Date takeDate, float takeFund,
			float cardBalance, String userNo) {
		super();
		this.personId = personId;
		this.takeDate = takeDate;
		this.takeFund = takeFund;
		this.cardBalance = cardBalance;
		this.userNo = userNo;
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public int getPersonId() {
		return personId;
	}

	public void setPersonId(int personId) {
		this.personId = personId;
	}

	public Date getTakeDate() {
		return takeDate;
	}

	public void setTakeDate(Date takeDate) {
		this.takeDate = takeDate;
	}

	public float getTakeFund() {
		return takeFund;
	}

	public void setTakeFund(float takeFund) {
		this.takeFund = takeFund;
	}

	public float getCardBalance() {
		return cardBalance;
	}

	public void setCardBalance(float cardBalance) {
		this.cardBalance = cardBalance;
	}

	public String getUserNo() {
		return userNo;
	}

	public void setUserNo(String userNo) {
		this.userNo = userNo;
	}
	
}
