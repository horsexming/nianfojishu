package com.task.entity.qimi;

import java.io.Serializable;

/**
 * 气密入库表 （表名 ta_gas）
 * 
 * @author 聂威
 * 
 */
public class Gas implements Serializable{
	private static final long serialVersionUID = 1L;
	private Integer id;
	private String markId;// 件号
	private String number;// 系列号
	private String addTime;// 添加时间
	private String userss;// 添加人
	private String selfCard;// 批次

	public String getUserss() {
		return userss;
	}

	public void setUserss(String userss) {
		this.userss = userss;
	}

	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public String getMarkId() {
		return markId;
	}

	public void setMarkId(String markId) {
		this.markId = markId;
	}

	public String getNumber() {
		return number;
	}

	public void setNumber(String number) {
		this.number = number;
	}

	public String getAddTime() {
		return addTime;
	}

	public void setAddTime(String addTime) {
		this.addTime = addTime;
	}

	public String getSelfCard() {
		return selfCard;
	}

	public void setSelfCard(String selfCard) {
		this.selfCard = selfCard;
	}

}
