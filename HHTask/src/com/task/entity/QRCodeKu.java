package com.task.entity;
/**
 * 二维码库:(ta_QRCodeKu)
 * @author 王晓飞
 *
 */
public class QRCodeKu implements java.io.Serializable{
	private static final long serialVersionUID =1L;

	private Integer id;
	private String no ;//二维码编码
	private String name;//文件名
	private Integer userId;//对应的userId;
	private String fatherNo;//上一层二维码的编码;
	private String fphoneNumber;//上一层产生二维码人的姓名
	private String fuserName;//上一层产生二维码人的手机号
	private String userName;//产生二维码人的姓名
	private String phoneNumber;//产生二维码人的手机号
	private String addtime;//生产时间;
	private String ischild;//是否拥有子码;(yes,No,null)
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	
	public String getNo() {
		return no;
	}

	public void setNo(String no) {
		this.no = no;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public Integer getUserId() {
		return userId;
	}
	public void setUserId(Integer userId) {
		this.userId = userId;
	}
	public String getUserName() {
		return userName;
	}
	public void setUserName(String userName) {
		this.userName = userName;
	}
	public String getAddtime() {
		return addtime;
	}
	public void setAddtime(String addtime) {
		this.addtime = addtime;
	}
	public String getIschild() {
		return ischild;
	}
	public void setIschild(String ischild) {
		this.ischild = ischild;
	}
	public String getPhoneNumber() {
		return phoneNumber;
	}
	public void setPhoneNumber(String phoneNumber) {
		this.phoneNumber = phoneNumber;
	}
	
	public String getFatherNo() {
		return fatherNo;
	}
	public void setFatherNo(String fatherNo) {
		this.fatherNo = fatherNo;
	}
	public String getFphoneNumber() {
		return fphoneNumber;
	}
	public void setFphoneNumber(String fphoneNumber) {
		this.fphoneNumber = fphoneNumber;
	}
	public String getFuserName() {
		return fuserName;
	}
	public void setFuserName(String fuserName) {
		this.fuserName = fuserName;
	}
	
	
	
}
