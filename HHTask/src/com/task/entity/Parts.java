package com.task.entity;

import java.io.Serializable;
import java.util.Date;

/***
 * @表 changeparts
 * @author liupei
 * 
 */
public class Parts implements Serializable {
	private static final long serialVersionUID = 1L;
	private Integer id;// ID
	private String barcode;// 生成的条码
	private String partName;// 更换零件的名字
	private String pictureNo;// 更换零件的规格
	private String num;// 更换零件的数量
	private String unit;// 单位
	private Date alarmTime;// 更换零件的时间
	private String more;// 单价

	private Maintenance maintenance;

	public Maintenance getMaintenance() {
		return maintenance;
	}

	public void setMaintenance(Maintenance maintenance) {
		this.maintenance = maintenance;
	}

	public Integer getId() {
		return id;
	}

	public String getBarcode() {
		return barcode;
	}

	public String getPartName() {
		return partName;
	}

	public String getPictureNo() {
		return pictureNo;
	}

	public String getNum() {
		return num;
	}

	public String getUnit() {
		return unit;
	}

	public Date getAlarmTime() {
		return alarmTime;
	}

	public String getMore() {
		return more;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public void setBarcode(String barcode) {
		this.barcode = barcode;
	}

	public void setPartName(String partName) {
		this.partName = partName;
	}

	public void setPictureNo(String pictureNo) {
		this.pictureNo = pictureNo;
	}

	public void setNum(String num) {
		this.num = num;
	}

	public void setUnit(String unit) {
		this.unit = unit;
	}

	public void setAlarmTime(Date alarmTime) {
		this.alarmTime = alarmTime;
	}

	public void setMore(String more) {
		this.more = more;
	}

}
