package com.task.entity;

import java.io.Serializable;

public class StatisticsStore implements Serializable{
	private static final long serialVersionUID = 1L;
	private String name;
	private String format;
	private String category;
	private Float price;
	private Float currentNum;
	private Float storageNum;
	private Float outNum;
	
	public StatisticsStore() {
		super();
	}

	public StatisticsStore(String name, String format, Float storageNum, Float outNum) {
		super();
		this.name = name;
		this.format = format;
		this.storageNum = storageNum;
		this.outNum = outNum;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getFormat() {
		return format;
	}

	public void setFormat(String format) {
		this.format = format;
	}

	public Float getStorageNum() {
		return storageNum;
	}

	public void setStorageNum(Float storageNum) {
		this.storageNum = storageNum;
	}

	public Float getOutNum() {
		return outNum;
	}

	public void setOutNum(Float outNum) {
		this.outNum = outNum;
	}

	public Float getCurrentNum() {
		return currentNum;
	}

	public void setCurrentNum(Float currentNum) {
		this.currentNum = currentNum;
	}

	public String getCategory() {
		return category;
	}

	public void setCategory(String category) {
		this.category = category;
	}

	public Float getPrice() {
		return price;
	}

	public void setPrice(Float price) {
		this.price = price;
	}

}
