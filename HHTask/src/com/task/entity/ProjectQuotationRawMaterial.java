package com.task.entity;

import java.io.Serializable;

/**
 * 项目报价_材料 详情参见PowerDesigner表为"材料成本"的类,里面全有注释
 * @author 马凯
 */
public class ProjectQuotationRawMaterial  implements Serializable {
	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;
	private Integer id;
	private String materialGrade;//原材料牌号
	private double density;//密度
	private double price;//原材料单价
	private double scrapPrice;//废料单价
	private double thickness;//厚度及公差
	private double unitLength;//料片长
	private double unitWidth;//料片宽
	private double coilScrap;//板材损耗
	private double nonQuality;//内部废品率
	private double netWeight;//零件净重
	private double grossWeight;//定额
	private double usage;//材料利用率
	private double materialCost;//原材料成本
	private double scrapValue;//回收价值
	private double totalCost;//实际成本
	private String rmType;//类型
	
	private String maker;//钢厂
	private String local;//国产/进口
	
	private ProjectQuotation root;//项目报价单

	public String getMaterialGrade() {
		return materialGrade;
	}
	public void setMaterialGrade(String materialGrade) {
		this.materialGrade = materialGrade;
	}
	public double getDensity() {
		return density;
	}
	public void setDensity(double density) {
		this.density = density;
	}
	public double getPrice() {
		return price;
	}
	public void setPrice(double price) {
		this.price = price;
	}
	public double getScrapPrice() {
		return scrapPrice;
	}
	public void setScrapPrice(double scrapPrice) {
		this.scrapPrice = scrapPrice;
	}
	public double getThickness() {
		return thickness;
	}
	public void setThickness(double thickness) {
		this.thickness = thickness;
	}
	public double getUnitLength() {
		return unitLength;
	}
	public void setUnitLength(double unitLength) {
		this.unitLength = unitLength;
	}
	public double getUnitWidth() {
		return unitWidth;
	}
	public void setUnitWidth(double unitWidth) {
		this.unitWidth = unitWidth;
	}
	public double getCoilScrap() {
		return coilScrap;
	}
	public void setCoilScrap(double coilScrap) {
		this.coilScrap = coilScrap;
	}
	public double getNonQuality() {
		return nonQuality;
	}
	public void setNonQuality(double nonQuality) {
		this.nonQuality = nonQuality;
	}
	public double getNetWeight() {
		return netWeight;
	}
	public void setNetWeight(double netWeight) {
		this.netWeight = netWeight;
	}
	public double getGrossWeight() {
		return grossWeight;
	}
	public void setGrossWeight(double grossWeight) {
		this.grossWeight = grossWeight;
	}
	public double getUsage() {
		return usage;
	}
	public void setUsage(double usage) {
		this.usage = usage;
	}
	public double getMaterialCost() {
		return materialCost;
	}
	public void setMaterialCost(double materialCost) {
		this.materialCost = materialCost;
	}
	public double getScrapValue() {
		return scrapValue;
	}
	public void setScrapValue(double scrapValue) {
		this.scrapValue = scrapValue;
	}
	public double getTotalCost() {
		return totalCost;
	}
	public void setTotalCost(double totalCost) {
		this.totalCost = totalCost;
	}
	public String getRmType() {
		return rmType;
	}
	public void setRmType(String rmType) {
		this.rmType = rmType;
	}
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public ProjectQuotation getRoot() {
		return root;
	}
	public void setRoot(ProjectQuotation root) {
		this.root = root;
	}
	public String getMaker() {
		return maker;
	}
	public void setMaker(String maker) {
		this.maker = maker;
	}
	public String getLocal() {
		return local;
	}
	public void setLocal(String local) {
		this.local = local;
	}
	
	
}
