package com.task.entity.dmltry;

import java.io.Serializable;

/**
 * 空调表 ta_Conditioning
 * 
 * @author duminglong
 *
 */
public class Conditioning  implements Serializable{

	private static final long serialVersionUID = 1L;
	private Integer id;
	// 控制空调ip
	private String conditioningip;
	// 所属区域
	private String region;
	// 绑定人员
	private String userid;

	public Conditioning(Integer id, String conditioningip, String region, String userid) {
		this.id = id;
		this.conditioningip = conditioningip;
		this.region = region;
		this.userid = userid;
	}

	public Conditioning() {
	}

	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public String getConditioningip() {
		return conditioningip;
	}

	public void setConditioningip(String conditioningip) {
		this.conditioningip = conditioningip;
	}

	public String getRegion() {
		return region;
	}

	public void setRegion(String region) {
		this.region = region;
	}

	public String getUserid() {
		return userid;
	}

	public void setUserid(String userid) {
		this.userid = userid;
	}

}
