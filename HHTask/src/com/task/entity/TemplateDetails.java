package com.task.entity;

/**
 * 考核明细表(表名:ta_hr_TemplateDetails)
 * 
 * @author 刘培
 */
public class TemplateDetails implements java.io.Serializable{

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;
	private int id;
	private String project;// 项目及内容
	private Float customScore;// 定制分数
	private String onLayer;// 上层
	private int layer;// 当前层
	private String isSroce;// 是否为打分项(yes/no)
	private Template template;// 模板类
	
	private Float weight;//相对于整体的权重比;
	private String oldOnLayer;// 上层(复制表时使用)
	private String isJida;// 是否机打(yes/no)
	private String jidaClasss;// 机打类别(order、)

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getProject() {
		return project;
	}

	public void setProject(String project) {
		this.project = project;
	}

	public Float getCustomScore() {
		return customScore;
	}

	public void setCustomScore(Float customScore) {
		this.customScore = customScore;
	}

	public String getOnLayer() {
		return onLayer;
	}

	public void setOnLayer(String onLayer) {
		this.onLayer = onLayer;
	}

	public int getLayer() {
		return layer;
	}

	public void setLayer(int layer) {
		this.layer = layer;
	}

	public Template getTemplate() {
		return template;
	}

	public void setTemplate(Template template) {
		this.template = template;
	}

	public String getIsSroce() {
		return isSroce;
	}

	public void setIsSroce(String isSroce) {
		this.isSroce = isSroce;
	}

	public String getOldOnLayer() {
		return oldOnLayer;
	}

	public void setOldOnLayer(String oldOnLayer) {
		this.oldOnLayer = oldOnLayer;
	}

	public String getIsJida() {
		return isJida;
	}

	public void setIsJida(String isJida) {
		this.isJida = isJida;
	}

	public String getJidaClasss() {
		return jidaClasss;
	}

	public void setJidaClasss(String jidaClasss) {
		this.jidaClasss = jidaClasss;
	}

	public Float getWeight() {
		return weight;
	}

	public void setWeight(Float weight) {
		this.weight = weight;
	}
	

}
