package com.task.entity.seal;

import java.io.Serializable;

/**
 * 印章借用申请 (表名 ： ta_BorrowSeal)
 * 
 * @author LiCong 16-08-30
 * 
 */
public class BorrowSeal  implements Serializable {
	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;
	private Integer id;
	private String slname;// 借用印章名称
	private String borrowAlso;// (借用/归还)
	private String jyName;// 借用人名称
	private String jyDept;// 借用人部门
	private String jyReason;// 借用原因
	private String jystatus;// 借用状态(借用中/已借用/待上传&归还中/已归还)
	private String status;
	private Integer epId;
	private String addTime;//
	private String filName;//归还文件名
	private Integer danganId;//档案申请Id

	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public String getFilName() {
		return filName;
	}

	public void setFilName(String filName) {
		this.filName = filName;
	}

	public String getSlname() {
		return slname;
	}

	public void setSlname(String slname) {
		this.slname = slname;
	}

	public String getBorrowAlso() {
		return borrowAlso;
	}

	public void setBorrowAlso(String borrowAlso) {
		this.borrowAlso = borrowAlso;
	}

	public String getJyName() {
		return jyName;
	}

	public void setJyName(String jyName) {
		this.jyName = jyName;
	}

	public String getJyDept() {
		return jyDept;
	}

	public void setJyDept(String jyDept) {
		this.jyDept = jyDept;
	}

	public String getJyReason() {
		return jyReason;
	}

	public void setJyReason(String jyReason) {
		this.jyReason = jyReason;
	}

	public String getJystatus() {
		return jystatus;
	}

	public void setJystatus(String jystatus) {
		this.jystatus = jystatus;
	}

	public String getStatus() {
		return status;
	}

	public void setStatus(String status) {
		this.status = status;
	}

	public Integer getEpId() {
		return epId;
	}

	public void setEpId(Integer epId) {
		this.epId = epId;
	}

	public String getAddTime() {
		return addTime;
	}

	public void setAddTime(String addTime) {
		this.addTime = addTime;
	}

	public Integer getDanganId() {
		return danganId;
	}

	public void setDanganId(Integer danganId) {
		this.danganId = danganId;
	}
}
