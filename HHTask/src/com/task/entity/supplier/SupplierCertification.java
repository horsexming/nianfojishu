package com.task.entity.supplier;
import java.io.Serializable;
import java.util.Set;

/*
    * @author fy
　　* @date 2018/6/22 15:52
　　* @Description: 供应商开发认证
　　* @param
　　* @return
　　* @throws
　　*/
public class SupplierCertification implements Serializable {
	private static final long serialVersionUID = 1L;
    private Integer id;
    private String applyUser;//申请人
    private String applyUserName;//申请人
    private String applyDept;//申请部门
    private String applyDate;//申请日期
    private String supplierName;//供应商名称
    private String supplierType;//供应商类型
    private String supplierTel;//供应商联系人！
    private String supplierContact;//供应商名称电话、！
    private String supplierAddress;//供应商地址
    private String applyReason;//申请原因
    private String state;//状态
    private String info;//信息调查说明
    private String info_attachment;//信息调查附件

    private Set<SupplierCertificationReviewContent> SupplierCertificationReviewContents ;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getApplyUser() {
        return applyUser;
    }

    public void setApplyUser(String applyUser) {
        this.applyUser = applyUser;
    }

    public String getApplyDept() {
        return applyDept;
    }

    public void setApplyDept(String applyDept) {
        this.applyDept = applyDept;
    }

    public String getApplyDate() {
        return applyDate;
    }

    public void setApplyDate(String applyDate) {
        this.applyDate = applyDate;
    }

    public String getSupplierName() {
        return supplierName;
    }

    public void setSupplierName(String supplierName) {
        this.supplierName = supplierName;
    }

    public String getSupplierType() {
        return supplierType;
    }

    public void setSupplierType(String supplierType) {
        this.supplierType = supplierType;
    }

    public String getSupplierTel() {
        return supplierTel;
    }

    public void setSupplierTel(String supplierTel) {
        this.supplierTel = supplierTel;
    }

    public String getSupplierContact() {
        return supplierContact;
    }

    public void setSupplierContact(String supplierContact) {
        this.supplierContact = supplierContact;
    }

    public String getSupplierAddress() {
        return supplierAddress;
    }

    public void setSupplierAddress(String supplierAddress) {
        this.supplierAddress = supplierAddress;
    }

    public String getApplyReason() {
        return applyReason;
    }

    public void setApplyReason(String applyReason) {
        this.applyReason = applyReason;
    }

    public String getState() {
        return state;
    }

    public void setState(String state) {
        this.state = state;
    }

    public Set<SupplierCertificationReviewContent> getSupplierCertificationReviewContents() {
        return SupplierCertificationReviewContents;
    }

    public void setSupplierCertificationReviewContents(Set<SupplierCertificationReviewContent> supplierCertificationReviewContents) {
        SupplierCertificationReviewContents = supplierCertificationReviewContents;
    }

    public String getInfo() {
        return info;
    }

    public void setInfo(String info) {
        this.info = info;
    }

    public String getInfo_attachment() {
        return info_attachment;
    }

    public void setInfo_attachment(String info_attachment) {
        this.info_attachment = info_attachment;
    }

    public String getApplyUserName() {
        return applyUserName;
    }

    public void setApplyUserName(String applyUserName) {
        this.applyUserName = applyUserName;
    }
}
