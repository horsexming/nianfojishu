package com.task.entity.menjin;

import java.io.Serializable;

/**
 * 串口信息接收发送记录表
 * 
 * @author Li_Cong 表名 ta_sp_SerialPtReceive 串口信息接收发送记录表
 */
public class SerialPtReceive implements Serializable {
	private static final long serialVersionUID = 1L;
	private Integer id;
	private String infor;// 内容
	private String inforHex;// 十六进制内容
	private Integer status;// 0：未显示/1：已显示
	private Integer comNum;// com口值
	private String addTime;// 添加时间
	// 关联设备上传出入记录
	// get set

	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public String getInforHex() {
		return inforHex;
	}

	public void setInforHex(String inforHex) {
		this.inforHex = inforHex;
	}

	public Integer getComNum() {
		return comNum;
	}

	public void setComNum(Integer comNum) {
		this.comNum = comNum;
	}

	public String getInfor() {
		return infor;
	}

	public void setInfor(String infor) {
		this.infor = infor;
	}

	public Integer getStatus() {
		return status;
	}

	public void setStatus(Integer status) {
		this.status = status;
	}

	public String getAddTime() {
		return addTime;
	}

	public void setAddTime(String addTime) {
		this.addTime = addTime;
	}

}
