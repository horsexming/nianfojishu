package com.task.entity.menjin;

import java.io.Serializable;

/**
 * 紧急开门
 * 
 * @author Li_Cong 11-08 表名 ta_mj_UrgentAccess 紧急开门申请
 */
public class UrgentAccess implements Serializable {
	private static final long serialVersionUID = 1L;
	private Integer id;
	private String code;// 提交人工号
	private String dept;// 提交人部门
	private String urgentReason;// 紧急申请原因
	private String urgentStruts;// 申请状态(申请/说明)
	private String useStruts;// 使用状态(未使用/已使用/)
	private String urgentCar;// 紧急进门对象
	private String urgentCarInTime;// 紧急对象开门时间
	private String addTime;// 添加时间
	private String updateTime;// 修改时间

	// get set
	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public String getCode() {
		return code;
	}

	public void setCode(String code) {
		this.code = code;
	}

	public String getDept() {
		return dept;
	}

	public void setDept(String dept) {
		this.dept = dept;
	}

	public String getUrgentReason() {
		return urgentReason;
	}

	public void setUrgentReason(String urgentReason) {
		this.urgentReason = urgentReason;
	}

	public String getUrgentStruts() {
		return urgentStruts;
	}

	public void setUrgentStruts(String urgentStruts) {
		this.urgentStruts = urgentStruts;
	}

	public String getAddTime() {
		return addTime;
	}

	public void setAddTime(String addTime) {
		this.addTime = addTime;
	}

	public String getUpdateTime() {
		return updateTime;
	}

	public void setUpdateTime(String updateTime) {
		this.updateTime = updateTime;
	}

	public String getUseStruts() {
		return useStruts;
	}

	public void setUseStruts(String useStruts) {
		this.useStruts = useStruts;
	}

	public String getUrgentCar() {
		return urgentCar;
	}

	public void setUrgentCar(String urgentCar) {
		this.urgentCar = urgentCar;
	}

	public String getUrgentCarInTime() {
		return urgentCarInTime;
	}

	public void setUrgentCarInTime(String urgentCarInTime) {
		this.urgentCarInTime = urgentCarInTime;
	}

}
