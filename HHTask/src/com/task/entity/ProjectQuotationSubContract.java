package com.task.entity;

import java.io.Serializable;

/**
 * 外购外协实体类
 * @author 马凯
 */
public class ProjectQuotationSubContract  implements Serializable {
	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;
	private Integer id;
	private String name;//外购,外协名称
	private String partNumber;//零件代号
	private String subSupplier;//次级供应商名称
	private String directed;//客户指定
	private Double quantity;//定额, pc或kg
	private Double unitPrice;//单价 RMB/Pc或Kg
	private Double cost;//零部件成本 RMB
	private ProjectQuotation root;
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getPartNumber() {
		return partNumber;
	}
	public void setPartNumber(String partNumber) {
		this.partNumber = partNumber;
	}
	public String getSubSupplier() {
		return subSupplier;
	}
	public void setSubSupplier(String subSupplier) {
		this.subSupplier = subSupplier;
	}
	public String getDirected() {
		return directed;
	}
	public void setDirected(String directed) {
		this.directed = directed;
	}
	public Double getQuantity() {
		return quantity;
	}
	public void setQuantity(Double quantity) {
		this.quantity = quantity;
	}
	public Double getUnitPrice() {
		return unitPrice;
	}
	public void setUnitPrice(Double unitPrice) {
		this.unitPrice = unitPrice;
	}
	public Double getCost() {
		return cost;
	}
	public void setCost(Double cost) {
		this.cost = cost;
	}
	public ProjectQuotation getRoot() {
		return root;
	}
	public void setRoot(ProjectQuotation root) {
		this.root = root;
	}
	
}
