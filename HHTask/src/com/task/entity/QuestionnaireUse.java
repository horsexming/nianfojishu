public class QuestionnaireUse implements java.io.Serializable{
	private static final long serialVersionUID =1L;

	private Integer id;
	private String content;//条例/内容
	private String status;//Yes/no;
	private QuestionnairePerson qpn;//
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	public QuestionnairePerson getQpn() {
		return qpn;
	}
	public void setQpn(QuestionnairePerson qpn) {
		this.qpn = qpn;
	}
	
	
}
