package com.task.entity;

import java.io.Serializable;

/**
 * 刷卡表
 * 
 * @author Administrator
 * @表名 STCard_ENP.dbo.KQ_BrushCard
 */
@SuppressWarnings("serial")
public class BrushCard implements Serializable {
	private static final long serialVersionUID = 1L;
	private Integer idKey;// 考勤ID主键
	private Integer personId;// 员工Id
	private String cardNo;// 卡号
	private String brushDate;// 打卡日期
	private String clockTime;// 打卡时间

	// getter和setter方法
	public Integer getPersonId() {
		return personId;
	}

	public void setPersonId(Integer personId) {
		this.personId = personId;
	}

	public String getCardNo() {
		return cardNo;
	}

	public void setCardNo(String cardNo) {
		this.cardNo = cardNo;
	}

	public String getClockTime() {
		return clockTime;
	}

	public void setClockTime(String clockTime) {
		this.clockTime = clockTime;
	}

	public Integer getIdKey() {
		return idKey;
	}

	public void setIdKey(Integer idKey) {
		this.idKey = idKey;
	}

	public String getBrushDate() {
		return brushDate;
	}

	public void setBrushDate(String brushDate) {
		this.brushDate = brushDate;
	}

}
