package com.task.entity;
//回款任务栏
public class TaHkShoppingCard implements java.io.Serializable {

	// Fields

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;
	private Integer id;//主键
	private String shoppingUserCode;//操作人工号
	private String shoppingUserName;//操作人姓名
	private String shoppingTime;//时间
	private String shoppingMore;//备注
	private TaHkSellSta haSellSta ;//对应的回款明细
	
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public String getShoppingUserCode() {
		return shoppingUserCode;
	}
	public void setShoppingUserCode(String shoppingUserCode) {
		this.shoppingUserCode = shoppingUserCode;
	}
	public String getShoppingUserName() {
		return shoppingUserName;
	}
	public void setShoppingUserName(String shoppingUserName) {
		this.shoppingUserName = shoppingUserName;
	}
	public String getShoppingTime() {
		return shoppingTime;
	}
	public void setShoppingTime(String shoppingTime) {
		this.shoppingTime = shoppingTime;
	}
	public String getShoppingMore() {
		return shoppingMore;
	}
	public void setShoppingMore(String shoppingMore) {
		this.shoppingMore = shoppingMore;
	}
	public TaHkSellSta getHaSellSta() {
		return haSellSta;
	}
	public void setHaSellSta(TaHkSellSta haSellSta) {
		this.haSellSta = haSellSta;
	}
	
	
}
