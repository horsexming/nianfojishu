package com.task.entity.sop;

/***
 * 工序人员
 * 
 * @author 刘培
 * 
 */
public class ProcessPeople  implements java.io.Serializable{
	private static final long serialVersionUID =1L;

	private Integer id;
	private Integer processId;// 工序id
	private String code;// 工号
	private String name;// 名称
	private String dept;// 部门
	private Float sum;// 提交量
	private String status;// 类型(操作/准备)

	private Integer addUserid;// 添加人id
	private String addUserCode;// 添加人工号
	private String addUserName;// 添加人名称

	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public Integer getProcessId() {
		return processId;
	}

	public void setProcessId(Integer processId) {
		this.processId = processId;
	}

	public String getCode() {
		return code;
	}

	public void setCode(String code) {
		this.code = code;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getDept() {
		return dept;
	}

	public void setDept(String dept) {
		this.dept = dept;
	}

	public String getStatus() {
		return status;
	}

	public void setStatus(String status) {
		this.status = status;
	}

	public Float getSum() {
		return sum;
	}

	public void setSum(Float sum) {
		this.sum = sum;
	}

	public Integer getAddUserid() {
		return addUserid;
	}

	public void setAddUserid(Integer addUserid) {
		this.addUserid = addUserid;
	}

	public String getAddUserCode() {
		return addUserCode;
	}

	public void setAddUserCode(String addUserCode) {
		this.addUserCode = addUserCode;
	}

	public String getAddUserName() {
		return addUserName;
	}

	public void setAddUserName(String addUserName) {
		this.addUserName = addUserName;
	}

}
