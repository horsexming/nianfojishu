package com.task.entity.sop;
/**
 * 不建表，传值使用
 * @author 王晓飞
 *
 */
public class DuiZhangWgPlan implements java.io.Serializable{
	private static final long serialVersionUID =1L;

	private String months;//月份
	private String yeras;//年份
	private Integer count;//条数
	private Float number;//总数量
	private Float money0;//总金额(数量*含税单价)
	private Float money1;//总金额
	private String type;
	
	public DuiZhangWgPlan() {
		super();
	}
	public DuiZhangWgPlan(String months, String yeras, Integer count,
			Float number, Float money0, Float money1) {
		super();
		this.months = months;
		this.yeras = yeras;
		this.count = count;
		this.number = number;
		this.money0 = money0;
		this.money1 = money1;
	}
	public String getMonths() {
		return months;
	}
	public void setMonths(String months) {
		this.months = months;
	}
	public String getYeras() {
		return yeras;
	}
	public void setYeras(String yeras) {
		this.yeras = yeras;
	}
	public Integer getCount() {
		return count;
	}
	public void setCount(Integer count) {
		this.count = count;
	}
	public Float getNumber() {
		return number;
	}
	public void setNumber(Float number) {
		this.number = number;
	}
	public Float getMoney0() {
		return money0;
	}
	public void setMoney0(Float money0) {
		this.money0 = money0;
	}
	public Float getMoney1() {
		return money1;
	}
	public void setMoney1(Float money1) {
		this.money1 = money1;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	
	
}
