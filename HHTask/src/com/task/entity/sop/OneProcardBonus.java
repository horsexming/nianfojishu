package com.task.entity.sop;
/**
 * 单工序奖金明细表（ta_sop_oneProcardBonus）
 * @author 贾辉辉
 *
 */
public class OneProcardBonus {
	private Integer id;//主键
	private ProcardPro procardPro;//领取的生产卡片的工序
	private String EnterStorTtime;//入库时间（提奖时间）
	private String code;//工号
	private String username;//员工姓名
	private float money;//该工序应领取的奖金
	private String processSubTime;//工序提交时间
	private Procard procard;//流水卡片信息
	private String more;
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public ProcardPro getProcardPro() {
		return procardPro;
	}
	public void setProcardPro(ProcardPro procardPro) {
		this.procardPro = procardPro;
	}
	public String getEnterStorTtime() {
		return EnterStorTtime;
	}
	public void setEnterStorTtime(String enterStorTtime) {
		EnterStorTtime = enterStorTtime;
	}
	public String getCode() {
		return code;
	}
	public void setCode(String code) {
		this.code = code;
	}
	public String getUsername() {
		return username;
	}
	public void setUsername(String username) {
		this.username = username;
	}
	public float getMoney() {
		return money;
	}
	public void setMoney(float money) {
		this.money = money;
	}
	public String getProcessSubTime() {
		return processSubTime;
	}
	public void setProcessSubTime(String processSubTime) {
		this.processSubTime = processSubTime;
	}
	public Procard getProcard() {
		return procard;
	}
	public void setProcard(Procard procard) {
		this.procard = procard;
	}
	public String getMore() {
		return more;
	}
	public void setMore(String more) {
		this.more = more;
	}
	
}
