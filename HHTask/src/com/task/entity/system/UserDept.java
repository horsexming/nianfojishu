package com.task.entity.system;

import java.io.Serializable;
/**
 * 用户部门server层表示用户拥有的部门权限 表名：ta_sys_UserDept
 * @author txb
 *
 */
public class UserDept implements Serializable{
	private static final long serialVersionUID = 1L;
 private Integer id;
 private Integer userId;
 private Integer deptId;
 private String code;//工号
 private String cardId;//卡号
 private String userName;
 private String deptName;//职能部门（显示时表示人员本身部门）
 private String deptName2;//显示时展示的职能部门
 private String duty;
 private Boolean hasChecked;
 private Integer backStage;
public Integer getId() {
	return id;
}
public void setId(Integer id) {
	this.id = id;
}
public Integer getUserId() {
	return userId;
}
public void setUserId(Integer userId) {
	this.userId = userId;
}
public Integer getDeptId() {
	return deptId;
}
public void setDeptId(Integer deptId) {
	this.deptId = deptId;
}
public String getUserName() {
	return userName;
}
public void setUserName(String userName) {
	this.userName = userName;
}
public String getDeptName() {
	return deptName;
}
public void setDeptName(String deptName) {
	this.deptName = deptName;
}
public String getCode() {
	return code;
}
public void setCode(String code) {
	this.code = code;
}
public String getCardId() {
	return cardId;
}
public void setCardId(String cardId) {
	this.cardId = cardId;
}
public Integer getBackStage() {
	return backStage;
}
public void setBackStage(Integer backStage) {
	this.backStage = backStage;
}
public String getDuty() {
	return duty;
}
public void setDuty(String duty) {
	this.duty = duty;
}
public String getDeptName2() {
	return deptName2;
}
public void setDeptName2(String deptName2) {
	this.deptName2 = deptName2;
}
public Boolean getHasChecked() {
	return hasChecked;
}
public void setHasChecked(Boolean hasChecked) {
	this.hasChecked = hasChecked;
}

 
}
