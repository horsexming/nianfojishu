package com.task.entity.system;

import java.io.Serializable;

/**
 * 
 * @author 王晓飞:(ta_sys_OptionRun)
 *
 */
public class OptionRun implements Serializable{
	private static final long serialVersionUID = 1L;
	private Integer id;
	private String name;
	private CircuitRun circuitRun;
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public CircuitRun getCircuitRun() {
		return circuitRun;
	}
	public void setCircuitRun(CircuitRun circuitRun) {
		this.circuitRun = circuitRun;
	}
	
}
