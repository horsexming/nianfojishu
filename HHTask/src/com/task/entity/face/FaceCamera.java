package com.task.entity.face;

import java.io.Serializable;

/**
 * 人脸摄像头列表
 * @author wcy
 *	ta_hr_facecamera
 */
public class FaceCamera implements Serializable{
	private static final long serialVersionUID = 1L;
	private Integer id;
	private String name;//摄像头名称
	private String position;//摄像头放置位置
	private String ip;//摄像头IP
	private Integer port;//摄像头端口号
	private String userName;//摄像头用户名
	private String password;//摄像头密码
	private Integer similarity;//比对相似度
	private String cameraUse;//摄像头用途
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getPosition() {
		return position;
	}
	public void setPosition(String position) {
		this.position = position;
	}
	public String getIp() {
		return ip;
	}
	public void setIp(String ip) {
		this.ip = ip;
	}
	public Integer getPort() {
		return port;
	}
	public void setPort(Integer port) {
		this.port = port;
	}
	public String getUserName() {
		return userName;
	}
	public void setUserName(String userName) {
		this.userName = userName;
	}
	public String getPassword() {
		return password;
	}
	public void setPassword(String password) {
		this.password = password;
	}
	public Integer getSimilarity() {
		return similarity;
	}
	public void setSimilarity(Integer similarity) {
		this.similarity = similarity;
	}
	public String getCameraUse() {
		return cameraUse;
	}
	public void setCameraUse(String cameraUse) {
		this.cameraUse = cameraUse;
	}
}
