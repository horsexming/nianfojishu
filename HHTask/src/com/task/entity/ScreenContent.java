package com.task.entity;

import java.io.Serializable;
import java.util.Date;
import java.util.Set;

/**
 * 屏幕轮播内容
 * @表名 ta_ScreenContent
 * @author fy
 * 
 */
public class ScreenContent implements Serializable{
	private static final long serialVersionUID = 1L;
	
	private Integer id;
	private Integer number;//编号
	private String name;
	private String screenUrl;// 地址
	private String status;//状态
	private String addTime;// 时间
	private Set<Screen> screen;
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public String getScreenUrl() {
		return screenUrl;
	}
	public void setScreenUrl(String screenUrl) {
		this.screenUrl = screenUrl;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	public String getAddTime() {
		return addTime;
	}
	public void setAddTime(String addTime) {
		this.addTime = addTime;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public Set<Screen> getScreen() {
		return screen;
	}
	public void setScreen(Set<Screen> screen) {
		this.screen = screen;
	}
	public Integer getNumber() {
		return number;
	}
	public void setNumber(Integer number) {
		this.number = number;
	}
	
	

}
