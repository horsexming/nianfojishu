package com.task.entity;

import java.io.Serializable;

/***
 * 审批流程短信提醒
 * 
 * @author 贾辉辉 数据库表 oa_messageAlerm
 */
public class OaMessageAlerm  implements Serializable {
	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	private Integer id;
	private String oaUserName;// 发送用户名
	private String realName;// 发送人
	private String accessPhone;// 短信接收电话
	private String sendDate;// 发送日期
	private String content;// 发送内容
	private String more;

	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public String getOaUserName() {
		return oaUserName;
	}

	public void setOaUserName(String oaUserName) {
		this.oaUserName = oaUserName;
	}

	public String getRealName() {
		return realName;
	}

	public void setRealName(String realName) {
		this.realName = realName;
	}

	public String getAccessPhone() {
		return accessPhone;
	}

	public void setAccessPhone(String accessPhone) {
		this.accessPhone = accessPhone;
	}

	public String getSendDate() {
		return sendDate;
	}

	public void setSendDate(String sendDate) {
		this.sendDate = sendDate;
	}

	public String getContent() {
		return content;
	}

	public void setContent(String content) {
		this.content = content;
	}

	public String getMore() {
		return more;
	}

	public void setMore(String more) {
		this.more = more;
	}

}