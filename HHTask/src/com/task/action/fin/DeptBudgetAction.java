package com.task.action.fin;

import java.util.List;

import com.opensymphony.xwork2.ActionSupport;
import com.task.Server.fin.budget.DeptBudgetServer;

/**
 * 部门预算action
 * 
 * @author jhh
 * 
 */
public class DeptBudgetAction extends ActionSupport {
	private DeptBudgetServer deptBudgetServer;
	private List list;
	private String tag;
	private String message;
	// 分页
	private String cpage = "1";
	private String total;
	private String url;
	private int pageSize = 15;
	private Integer id;// 主键
	private String startDate;// 开始时间
	private String endDate;// 截止时间

	public DeptBudgetServer getDeptBudgetServer() {
		return deptBudgetServer;
	}

	public void setDeptBudgetServer(DeptBudgetServer deptBudgetServer) {
		this.deptBudgetServer = deptBudgetServer;
	}

	public List getList() {
		return list;
	}

	public void setList(List list) {
		this.list = list;
	}

	public String getTag() {
		return tag;
	}

	public void setTag(String tag) {
		this.tag = tag;
	}

	public String getMessage() {
		return message;
	}

	public void setMessage(String message) {
		this.message = message;
	}

	public String getCpage() {
		return cpage;
	}

	public void setCpage(String cpage) {
		this.cpage = cpage;
	}

	public String getTotal() {
		return total;
	}

	public void setTotal(String total) {
		this.total = total;
	}

	public String getUrl() {
		return url;
	}

	public void setUrl(String url) {
		this.url = url;
	}

	public int getPageSize() {
		return pageSize;
	}

	public void setPageSize(int pageSize) {
		this.pageSize = pageSize;
	}

	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public String getStartDate() {
		return startDate;
	}

	public void setStartDate(String startDate) {
		this.startDate = startDate;
	}

	public String getEndDate() {
		return endDate;
	}

	public void setEndDate(String endDate) {
		this.endDate = endDate;
	}

}
