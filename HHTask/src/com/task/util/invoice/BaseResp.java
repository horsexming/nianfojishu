package com.task.util.invoice;

public class BaseResp<T> {

    public BaseResp(T data){
        this.data = data;
    }

    public BaseResp(int code, String msg, T data){
        this.data = data;
        this.code = code;
        this.msg = msg;
    }

    public int code = 0;

    public String msg = null;

    public T data = null;

}
