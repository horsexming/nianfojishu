package com.huawei.openapi.openaipexample.client.http;

/*****
 * 条码锁定
 * 
 * @author liupei
 * 
 */
public class TbBarcodeLock {
	String FBarcode = "";// 条码
	String FMatCode = "";// 料号
	String FMatName = "";// 品名
	String FLocked = "";// 锁定
	String FLockReason = "";// 锁定原因
	String FUnlockReason = "";// 解锁原因
	String FLockUser = "";// 操作人
	String FLockDate = "";// 操作日期
	String lockNo = "";// 隔离单号

	public TbBarcodeLock() {
	}

	public TbBarcodeLock(String FBarcode, String FMatCode, String FMatName,
			String FLocked, String FLockReason, String FUnlockReason,
			String FLockUser, String FLockDate, String LockNo) {
		this.FBarcode = FBarcode;
		this.FMatCode = FMatCode;
		this.FMatName = FMatName;
		this.FLocked = FLocked;
		this.FLockReason = FLockReason;
		this.FUnlockReason = FUnlockReason;
		this.FLockUser = FLockUser;
		this.FLockDate = FLockDate;
		this.lockNo = LockNo;
	}

	public String getFBarcode() {
		return FBarcode;
	}

	public void setFBarcode(String fBarcode) {
		FBarcode = fBarcode;
	}

	public String getFMatCode() {
		return FMatCode;
	}

	public void setFMatCode(String fMatCode) {
		FMatCode = fMatCode;
	}

	public String getFMatName() {
		return FMatName;
	}

	public void setFMatName(String fMatName) {
		FMatName = fMatName;
	}

	public String getFLocked() {
		return FLocked;
	}

	public void setFLocked(String fLocked) {
		FLocked = fLocked;
	}

	public String getFLockReason() {
		return FLockReason;
	}

	public void setFLockReason(String fLockReason) {
		FLockReason = fLockReason;
	}

	public String getFUnlockReason() {
		return FUnlockReason;
	}

	public void setFUnlockReason(String fUnlockReason) {
		FUnlockReason = fUnlockReason;
	}

	public String getFLockUser() {
		return FLockUser;
	}

	public void setFLockUser(String fLockUser) {
		FLockUser = fLockUser;
	}

	public String getFLockDate() {
		return FLockDate;
	}

	public void setFLockDate(String fLockDate) {
		FLockDate = fLockDate;
	}

	public String getLockNo() {
		return lockNo;
	}

	public void setLockNo(String lockNo) {
		this.lockNo = lockNo;
	}

}
