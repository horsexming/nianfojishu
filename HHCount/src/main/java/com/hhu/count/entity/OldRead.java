package com.hhu.count.entity;

import java.util.Date;

public class OldRead {
	
	private String id;
	
	private Date readerDate;
	
	private String myreaderId;

	private String oldReading;
	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public Date getReaderDate() {
		return readerDate;
	}

	public void setReaderDate(Date readerDate) {
		this.readerDate = readerDate;
	}

	public String getMyreaderId() {
		return myreaderId;
	}

	public void setMyreaderId(String myreaderId) {
		this.myreaderId = myreaderId;
	}

	public String getOldReading() {
		return oldReading;
	}

	public void setOldReading(String oldReading) {
		this.oldReading = oldReading;
	}

	
	
	

}
