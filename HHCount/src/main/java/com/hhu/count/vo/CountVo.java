package com.hhu.count.vo;
/*按照年月日统计*/
public class CountVo {
	
	private String readerDate;
	private String courseNum;
	private String readingNum;
	public String getReaderDate() {
		return readerDate;
	}
	public void setReaderDate(String readerDate) {
		this.readerDate = readerDate;
	}
	public String getCourseNum() {
		return courseNum;
	}
	public void setCourseNum(String courseNum) {
		this.courseNum = courseNum;
	}
	public String getReadingNum() {
		return readingNum;
	}
	public void setReadingNum(String readingNum) {
		this.readingNum = readingNum;
	}
	
	

}
